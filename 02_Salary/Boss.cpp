#include "Boss.h"

///////////////////////////////////////////////////////////////////////////
//CTor
///////////////////////////////////////////////////////////////////////////
Boss::Boss(std::string const& firstname, std::string const& name, std::string const& shortname,
		   std::string const& insurance, size_t const& entryyear):
	Employee{firstname, name, shortname, insurance, entryyear, TWorker::Boss}, mSalary{0.0}
{
}

///////////////////////////////////////////////////////////////////////////
//Setter & Getter
///////////////////////////////////////////////////////////////////////////
double Boss::GetSalary() const
{
	return mSalary;
}

void Boss::SetSalary(double salary)
{
	//Salary can´t be below 0
	if (salary < 0)
	{
		std::cerr << "Salary has to be positive" << std::endl;
	}
	else
	{
		mSalary = salary;
	}
}

///////////////////////////////////////////////////////////////////////////
//Template Method to Print the Salary
///////////////////////////////////////////////////////////////////////////
void Boss::PrintSalary(std::ostream& ost) const
{
	ost << "Gehalt: " << mSalary << Currency << std::endl;
}
